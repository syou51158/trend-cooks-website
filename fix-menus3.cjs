async function fixMenus() {
  const { data } = await supabase.from('menus').select('id, name');
  let count = 0;
  for (const item of data) {
    for (const m of mapping) {
      if (item.name && item.name.includes(m.bad.substring(0, Math.min(m.bad.length, 5)))) {
        console.log('Fixing', item.name, '->', m.good);
        await supabase.from('menus').update({ name: m.good }).eq('id', item.id);
        count++;
        break;
      }
    }
  }
  console.log(`Fixed ${count} items.`);
}
